    {
        if (request()->ajax())
        {
            $tenants = Tenant::get();
            return DataTables()
            ->of($tenants)
            ->editColumn('created_at', function(Tenant $tenant){
                return $tenant->created_at->format('Y-m-d H:m:s');
            })
            ->toJson();
        }
        return view('landlord.tenant.index');
    }

